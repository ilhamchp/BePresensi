    public function storeMahasiswa($data){
        $user = new User;
        $user->email = $data['nim'] . '@yopmail.com';
        $user->password = md5($data['nim']);
        $user->save();

        $kelas = Kelas::find($data['kd_kelas']);

        $mahasiswa = new Mahasiswa;
        $mahasiswa->nim = $data['nim'];
        $mahasiswa->nama_mahasiswa = $data['nama'];
        $mahasiswa->foto_mahasiswa = $data['nim'] . '.jpg';
        $mahasiswa->user()->associate($user);
        $mahasiswa->kelas()->associate($kelas);
        $mahasiswa->save();
    }
